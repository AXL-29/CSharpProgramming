internal class MainClass
{
    public static void Main(string[] args)
    {
        Console.WriteLine("Hello, World!"); // Console.WriteLine() is used to print text to the console.
        Console.Beep(); // Console.Beep() produces a beep sound from the console.

        Console.WriteLine();    // Type cw + Tab to generate Console.WriteLine()
        Console.WriteLine("\t Jade Mark Gimpao");   // Type \t  to add a tab space before the text
        Console.WriteLine("Ja\bde");    // Type \b to remove the character before it.
        Console.WriteLine("Ja\nde");    // Type \n to add a new line before the text.

        Console.ReadKey(); // Console.ReadKey() waits for a key press before closing the console window.

    }
}